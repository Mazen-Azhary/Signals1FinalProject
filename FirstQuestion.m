fs = 50;
figure;
subplot(2, 2, 1);
t = linspace(-4, 4, fs * 8); 
x = exp(-abs(t) / 5);
u_rise = t >= -1;
u_fall = t <= 3;
x_step = x .* (u_rise .* u_fall);
plot(t, x_step);
xlim([-4, 4]);
ylim([0, 2]);
title('y(t)');
grid on;
subplot(2, 2, 2);
t2 = linspace(-4, 4, fs * 8); 
x2 = exp(-abs(3 * t2) / 5);
u_rise2 = t2 >= -1/3;
u_fall2 = t2 < 1;
x2_step = x2 .* (u_rise2 .* u_fall2); 
plot(t2, x2_step);
xlim([-4, 4]);
ylim([0, 2]);
title('y1(t)');
grid on;
subplot(2, 2, 3);
t3 = linspace(-4, 4, fs * 8); 
x3 = exp(-abs(t3 + 2) / 5);
u_rise3 = t3 >= -3; 
u_fall3 = t3 < 1;
x3_step = x3 .* (u_rise3 .* u_fall3);
plot(t3, x3_step);
xlim([-4, 4]);
ylim([0, 2]);
title('y2(t)');
grid on;
subplot(2, 2, 4);
t4 = linspace(-4, 4, fs * 8);
x4 = exp(-abs(4 - 2 * t4) / 5);
u_rise4 = t4 >= 0.5;
u_fall4 = t4 < 2.5;
x4_step = x4 .* (u_rise4 .* u_fall4);
plot(t4, x4_step);
xlim([-4, 4]);
ylim([0, 2]);
title('y3(t)');
grid on;
